char	*ft_strstr(const char *str1, const char *str2)
{
	const char *s1;

	if (*str2 == '\0')
		return ((char*)str1);
	while (*str1 != '\0')
	{
		s1 = str1;
		while (*s1 != '\0' && *s1 == *str2)
		{
			s1++;
			str2++;
		}
		if (*str2 == '\0')
			return ((char*)str1);
		str1++;
	}
	return (0);
}
